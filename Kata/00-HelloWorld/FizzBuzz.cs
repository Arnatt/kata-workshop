namespace Kata.FizzBuzz;

public class FizzBuzz
{
    public string SayFizzBuzz(int number)
    {
        if(number == 1)
            return "1";
        if(number == 2)
            return "2";
        if(number == 3)
            return "Fizz";
        if(number == 6)
            return "Fizz";
        return null;
    }
}

